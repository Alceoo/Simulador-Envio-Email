/*Do while loop*/

/*A diferencia del for y del while, el do while, se ejecuta al menos una vez y después verifica la 
condicion, pero almenos se ejecuta una vez*/

let i = 0; 
/*Es básicamente, voy a ejecutar el código del do, mientras se cumpla la condicion que ponga en el while*/
do {
console.log(`Numero: ${i}`);

    i++ //Incremento
} while(i < 10); //Condicion

/*Éste se va a ejecutar almenos una vez, no importa si no se cumple la condicion, esto se
va a ejecutar al menos una vez*/



