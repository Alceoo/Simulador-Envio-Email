/*  Crear funciones en Javascript-funcion expresiva y funcion declarativa*/

/*Las funciones son una serie de procedimientos o instrucciones, es decir líneas de código
Las funciones son reutilizables, podemos tener una funcion que valide un formulario y esa misma 
la podemos tener en todos los formularios*/

/*Existen dos formas de crear funciones en javascript, la declarativa y la expresiva*/

//Declaracion de funcion

function sumar() {
    console.log(2+200);
}
//Mandar llamar la funcion(sólo requieres el nombre de la funcion y paréntesis)
sumar();
//se pueden mandar llamar varias veces, poniéndo pss varias veces el nombre de la funcion jaja
sumar();
sumar();


//Expresion de funcion 
const sumar2 = function(){
    console.log(3+4);
}

sumar2();
/*Ésta es la segunda forma de mandar llamar a las funciones */















